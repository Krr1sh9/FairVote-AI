from __future__ import annotations

import inspect

import numpy as np
import pytest

torch = pytest.importorskip("torch")
# Keep PyTorch unit tests fast and deterministic on small CI machines.
torch.set_num_threads(1)

from fairvote.inference.mrp.rr_neural_mrp import RRNeuralMRPModel
from fairvote.privacy.mechanisms.kary_rr import privatize_many


def _simple_data(n: int = 160, *, seed: int = 123, epsilon: float = 1.5, k: int = 3):
    """Small synthetic RR dataset with a real demographic signal.

    True labels are created only inside the test fixture so that reported labels
    can be generated by the same k-ary RR mechanism used elsewhere in the
    project. The model under test receives only ``X`` and ``y_reported``.
    """

    rng = np.random.default_rng(seed)
    x0 = rng.integers(0, 2, size=n)
    x1 = rng.normal(size=n)
    X = np.column_stack([np.ones(n), x0, x1]).astype(float)

    true = np.where(x0 == 1, 0, 1)
    # Add a small third-class tail so k=3 is non-degenerate.
    tail = rng.random(n) < 0.08
    true = np.where(tail, 2, true).astype(int)

    y_reported = privatize_many(true, epsilon=epsilon, k=k, rng=rng)
    return X, true, y_reported


def _assert_probability_rows(p: np.ndarray, *, n: int, k: int) -> None:
    assert p.shape == (n, k)
    assert np.all(np.isfinite(p))
    assert np.all(p >= 0.0)
    assert np.allclose(p.sum(axis=1), 1.0, atol=1e-6)


def test_model_can_be_instantiated_with_configurable_neural_settings():
    model = RRNeuralMRPModel(
        k=3,
        epsilon=1.0,
        hidden_layers=(8, 4),
        dropout=0.2,
        weight_decay=1e-3,
        seed=42,
    )

    assert model.k == 3
    assert model.epsilon == 1.0
    assert model.hidden_layers == (8, 4)
    assert model.dropout == pytest.approx(0.2)
    assert model.weight_decay == pytest.approx(1e-3)
    assert not model.is_fitted
    assert model.A_np.shape == (3, 3)
    assert np.allclose(model.A_np.sum(axis=1), 1.0)


def test_fit_signature_uses_reported_not_true_labels():
    # Regression guard: the fit method should accept "y_reported" (privatised
    # labels) and must never expose a parameter named "y_true" or similar,
    # since the model must not train on unprivatised data.
    sig = inspect.signature(RRNeuralMRPModel.fit)
    assert "X" in sig.parameters
    assert "y_reported" in sig.parameters
    assert "y_true" not in sig.parameters
    assert "true_categories" not in sig.parameters
    assert "true" not in sig.parameters


def test_fit_runs_on_small_synthetic_dataset_without_crashing():
    X, _true, y_reported = _simple_data(n=80, seed=1)
    model = RRNeuralMRPModel(k=3, epsilon=1.5, hidden_layers=(6,), dropout=0.1, seed=7)

    info = model.fit(X, y_reported, steps=3, batch_size=32, lr=0.01)

    assert model.is_fitted
    assert info.steps == 3
    assert np.isfinite(info.final_loss)
    assert info.history is None


def test_predict_true_proba_shape_and_probability_normalisation():
    X, _true, y_reported = _simple_data(n=100, seed=2)
    model = RRNeuralMRPModel(k=3, epsilon=1.5, hidden_layers=(8, 4), dropout=0.1, seed=4)
